// Hàm thêm game vào danh sách yêu thích
export function toggleFavorite(game, gameName, heartIcon) {
  const index = favoriteGames.findIndex((fav) => fav.name === gameName);
  if (index === -1) {
    // Nếu game chưa có trong danh sách yêu thích, thêm vào
    favoriteGames.push(game);
    heartIcon.style.color = "red"; // Thay đổi màu trái tim thành đỏ
  } else {
    // Nếu game đã có trong danh sách yêu thích, xóa ra khỏi danh sách
    favoriteGames.splice(index, 1);
    heartIcon.style.color = "white"; // Thay đổi màu trái tim thành trắng
  }
  localStorage.setItem("favoriteGames", JSON.stringify(favoriteGames));
}

// Hàm thêm game vào giỏ hàng
export function toggleCart(game, gameName, cartIcon) {
  const index = carts.findIndex((cart) => cart.name === gameName);
  if (index === -1) {
    carts.push(game);
    cartIcon.className = "bi bi-bag-dash-fill position-absolute bg-primary";
  } else {
    carts.splice(index, 1);
    cartIcon.className = "bi bi-bag-plus-fill position-absolute bg-primary";
  }
  localStorage.setItem("carts", JSON.stringify(carts));
}





